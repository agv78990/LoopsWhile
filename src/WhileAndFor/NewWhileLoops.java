package WhileAndFor;

public class NewWhileLoops {

    public static void main(String[] args) {
        int a=1;
        while(a<=10){
            System.out.println("serial number"+ a);
            a+=1;
        }

        int e=0;
        while(e<=10){
            System.out.println("even numbers "+ e);
            e+=2;
        }


        int i=20;
        do{
            System.out.println("print this "+ i);
        i++;}
        while(i<=10);



    }




}
