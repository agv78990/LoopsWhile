package WhileAndFor;

public class For {

    public static void main(String[] args) {
        for(int i=1; (i<=10); i++){
            System.out.println("serial number "+ i);
        }

        for(int a=0; (a<=10); a+=2){
            System.out.println(" even "+ a);
        }
        System.out.println("program exited");

        for(int i=10; (i>=1); i--){
            System.out.println("decreasing " + i);
        }



    }





}
